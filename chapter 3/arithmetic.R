large <- 5
small <- 2

cat("Addition: ", large + small, "\n")
cat("Substraction: ", large - small, "\n")
cat("Multiplication: ", large * small, "\n")

cat("Division: ", large / small, "\n")
cat("Integer Division:", large %/% small, "\n")

cat("Exponetiation: ", large ^ small, "\n")

cat("Remainder", large %% small, "\n")